// Язык обучения/интерфейса: ru или en.
// Выбранный язык хранится в localStorage и определяет,
// на каком языке показываются переводы фраз и подписи интерфейса.

const LANG_KEY = "sitkaz_lang";

export function loadLang() {
  if (typeof window === "undefined") return "ru";
  try {
    const saved = localStorage.getItem(LANG_KEY);
    if (saved === "ru" || saved === "en") return saved;
  } catch {}
  return "ru";
}

export function saveLang(lang) {
  try { localStorage.setItem(LANG_KEY, lang); } catch {}
}

// Перевод фразы/объекта: берём поле нужного языка, откат на ru.
export const tr = (obj, lang, field = "") => {
  if (!obj) return "";
  const base = field || (lang === "en" ? "en" : "ru");
  return obj[base] ?? obj.ru ?? obj.en ?? "";
};

